/*
Common HTTP Methods:
GET:
Used to retrieve data from a specified resource. 

POST:
Used to submit data to be processed to a specified resource. It often leads to state changes or data creation on the server. 

PUT:
Used to replace an existing resource with the enclosed entity. It can also create a new resource if one doesn't exist. PUT requests are idempotent. 

PATCH:
Used to apply partial modifications to a resource, unlike PUT which replaces the entire resource. 

DELETE:
Used to request the removal of a resource. 

*/

